package practice.practiceDTNT;

public class WhileLoopDuWhileLoop {
    public static void main(String[] args) {
        //ne zaman while loop ne zaman du while loop

        //0dan baslayip 3 e kadar birer birer artan kodu yaziniz
        int i = 0;
        while (i<3){
            System.out.println(i+" ");
            i++;
        }




        do {
            System.out.println(i+" ");
            i++;
        }while (i<3);








    }
}
